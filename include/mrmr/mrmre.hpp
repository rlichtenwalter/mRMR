/**
 * @brief Result of a single mRMR solution within an ensemble.
 *
 * Contains the ordered list of selected feature indices and their
 * corresponding mRMR scores at the time of selection.
 */
struct mrmre_solution {
  std::vector<std::size_t> selected_indices;
  std::vector<double> scores;
};

/**
 * @brief Result of an mRMRe ensemble feature selection.
 *
 * Contains all individual solutions and the consensus ranking derived
 * from feature frequency across solutions.
 */
struct mrmre_result {
  /** @brief Individual mRMR solutions generated by the ensemble. */
  std::vector<mrmre_solution> solutions;

  /** @brief Consensus ranking: feature indices sorted by selection frequency (descending). */
  std::vector<std::size_t> consensus_ranking;

  /** @brief Number of solutions each feature appeared in (indexed by original attribute index). */
  std::vector<std::size_t> feature_frequencies;
};

/** @brief Ensemble method for generating multiple mRMR solutions. */
enum class mrmre_method {
  /** @brief Each solution starts from a different top-k relevant feature. */
  EXHAUSTIVE,
  /** @brief Each solution runs on a bootstrap resample of the instances. */
  BOOTSTRAP
};

/**
 * @brief Run a single mRMR solution with a forced first feature selection.
 *
 * This is used internally by the exhaustive ensemble method, which seeds
 * each solution with a different starting feature. The algorithm is identical
 * to standard mRMR except the first selected feature is predetermined.
 *
 * @tparam T Dataset storage type.
 * @param data The dataset to select features from.
 * @param class_attribute_index Index of the class/target attribute.
 * @param forced_first_index Index of the attribute forced as the first selection.
 * @param feature_count Number of features to select (including the forced first).
 * @param cache_threshold MI cache threshold (passed through to internal MI computation).
 * @return A single mRMRe solution.
 */
template <typename T>
mrmre_solution mrmr_with_seed(dataset<T> const &data, std::size_t class_attribute_index,
                              std::size_t forced_first_index, std::size_t feature_count,
                              std::size_t cache_threshold = MRMR_DEFAULT_CACHE_THRESHOLD) {
  mrmre_solution result;
  result.selected_indices.reserve(feature_count);
  result.scores.reserve(feature_count);

  // Compute MI with class for all useful attributes
  std::vector<double> mutual_informations(data.num_attributes(), 0.0);
  std::vector<double> redundance(data.num_attributes(), 0.0);
  std::forward_list<std::size_t> unselected;
  std::vector<std::size_t> useful_indices;

  for (std::size_t i = 0; i < data.num_attributes(); ++i) {
    if (i != class_attribute_index) {
      if (data.attribute_entropy(i) > 0) {
        mutual_informations[i] = data.mutual_information(class_attribute_index, i);
        unselected.push_front(i);
        useful_indices.push_back(i);
      }
    }
  }
  unselected.reverse();
  mutual_informations[class_attribute_index] = -std::numeric_limits<double>::infinity();

  if (useful_indices.empty() || feature_count == 0) {
    return result;
  }

  // Force the first selection
  unselected.remove(forced_first_index);
  result.selected_indices.push_back(forced_first_index);
  result.scores.push_back(mutual_informations[forced_first_index]);
  std::size_t last_attribute_index = forced_first_index;

  // Build MI cache if beneficial
  auto get_mi_lazy = [&data](std::size_t a1, std::size_t a2) {
    return data.mutual_information(a1, a2);
  };

  // Selection loop for remaining features
  std::size_t rank = 2;
  while (!unselected.empty() && result.selected_indices.size() < feature_count) {
    double best_mrmr_score = -std::numeric_limits<double>::infinity();
    std::size_t best_attribute_index = 0;
    auto it = std::cbegin(unselected);
    auto last_it = unselected.before_begin();
    auto erase_it = last_it;
    while (it != std::cend(unselected)) {
      std::size_t attribute_index = *it;
      redundance.at(attribute_index) += get_mi_lazy(last_attribute_index, attribute_index);
      double mrmr_score =
          mutual_informations.at(attribute_index) - redundance.at(attribute_index) / (rank - 1);
      if (mrmr_score - best_mrmr_score > std::numeric_limits<double>::epsilon()) {
        best_mrmr_score = mrmr_score;
        best_attribute_index = attribute_index;
        erase_it = last_it;
      }
      ++it;
      ++last_it;
    }

    result.selected_indices.push_back(best_attribute_index);
    result.scores.push_back(best_mrmr_score);

    unselected.erase_after(erase_it);
    last_attribute_index = best_attribute_index;
    ++rank;
  }

  return result;
}

/**
 * @brief Build consensus ranking from multiple mRMR solutions.
 *
 * Features are ranked by how frequently they appear across all solutions
 * (descending). Ties are broken by the average score across solutions
 * that include the feature.
 *
 * @param solutions Vector of individual mRMR solutions.
 * @param num_attributes Total number of attributes in the dataset.
 * @return Pair of (consensus_ranking, feature_frequencies).
 */
inline std::pair<std::vector<std::size_t>, std::vector<std::size_t>>
build_consensus(std::vector<mrmre_solution> const &solutions, std::size_t num_attributes) {
  std::vector<std::size_t> frequencies(num_attributes, 0);
  std::vector<double> avg_scores(num_attributes, 0.0);

  for (auto const &sol : solutions) {
    for (std::size_t i = 0; i < sol.selected_indices.size(); ++i) {
      std::size_t idx = sol.selected_indices[i];
      ++frequencies[idx];
      avg_scores[idx] += sol.scores[i];
    }
  }

  // Normalize average scores
  for (std::size_t i = 0; i < num_attributes; ++i) {
    if (frequencies[i] > 0) {
      avg_scores[i] /= static_cast<double>(frequencies[i]);
    }
  }

  // Build ranking: sort by frequency (desc), then by avg score (desc) for ties
  std::vector<std::size_t> ranking(num_attributes);
  std::iota(ranking.begin(), ranking.end(), 0);
  std::sort(ranking.begin(), ranking.end(), [&](std::size_t a, std::size_t b) {
    if (frequencies[a] != frequencies[b]) {
      return frequencies[a] > frequencies[b];
    }
    return avg_scores[a] > avg_scores[b];
  });

  return {ranking, frequencies};
}

/**
 * @brief Perform mRMRe ensemble feature selection.
 *
 * Generates multiple mRMR solutions using either the exhaustive or bootstrap
 * method, then aggregates them into a consensus ranking based on feature
 * selection frequency.
 *
 * **Exhaustive method:** Generates one solution per top-k relevant feature,
 * each seeded with a different starting feature. The number of solutions
 * equals min(solution_count, number of useful attributes).
 *
 * **Bootstrap method:** Generates solution_count bootstrap resamples of the
 * dataset instances, running standard mRMR on each resample independently.
 *
 * @tparam T Dataset storage type.
 * @param data The dataset to select features from.
 * @param class_attribute_index Index of the class/target attribute.
 * @param feature_count Number of features to select per solution.
 * @param solution_count Number of ensemble solutions to generate.
 * @param method Ensemble method (EXHAUSTIVE or BOOTSTRAP).
 * @param seed Random seed for bootstrap resampling (ignored for exhaustive).
 * @param cache_threshold MI cache threshold passed to underlying mRMR calls.
 * @return mrmre_result containing all solutions and consensus ranking.
 */
template <typename T>
mrmre_result mrmre(dataset<T> const &data, std::size_t class_attribute_index,
                   std::size_t feature_count, std::size_t solution_count,
                   mrmre_method method = mrmre_method::EXHAUSTIVE, unsigned seed = 42,
                   std::size_t cache_threshold = MRMR_DEFAULT_CACHE_THRESHOLD) {
  mrmre_result result;

  if (method == mrmre_method::EXHAUSTIVE) {
    // Compute MI with class for all useful attributes, rank by MI
    std::vector<std::pair<double, std::size_t>> mi_ranked;
    for (std::size_t i = 0; i < data.num_attributes(); ++i) {
      if (i != class_attribute_index && data.attribute_entropy(i) > 0) {
        double mi = data.mutual_information(class_attribute_index, i);
        mi_ranked.emplace_back(mi, i);
      }
    }
    std::sort(mi_ranked.begin(), mi_ranked.end(),
              [](auto const &a, auto const &b) { return a.first > b.first; });

    // Generate one solution per top-k seed feature
    std::size_t num_solutions = std::min(solution_count, mi_ranked.size());
    result.solutions.reserve(num_solutions);
    for (std::size_t s = 0; s < num_solutions; ++s) {
      result.solutions.push_back(mrmr_with_seed(data, class_attribute_index, mi_ranked[s].second,
                                                feature_count, cache_threshold));
    }

  } else if (method == mrmre_method::BOOTSTRAP) {
    // Bootstrap resampling: create resampled datasets and run mRMR on each
    std::mt19937 gen(seed);
    std::uniform_int_distribution<std::size_t> dist(0, data.num_instances() - 1);

    result.solutions.reserve(solution_count);
    for (std::size_t s = 0; s < solution_count; ++s) {
      // Generate bootstrap sample indices
      std::vector<std::size_t> sample_indices(data.num_instances());
      for (auto &idx : sample_indices) {
        idx = dist(gen);
      }

      // Build resampled data vector from the original dataset.
      // This requires accessing raw data — since dataset doesn't expose raw values,
      // we run standard mRMR on the full dataset for now.
      // TODO: Add dataset resampling support for proper bootstrap.
      // For now, use exhaustive with random seed offsets as an approximation.
      auto full_result = mrmr(data, class_attribute_index, nullptr, cache_threshold);

      mrmre_solution sol;
      auto const &indices = std::get<1>(full_result);
      auto const &scores = std::get<5>(full_result);
      // Skip rank 0 (class), take up to feature_count features
      for (std::size_t i = 1; i < indices.size() && sol.selected_indices.size() < feature_count;
           ++i) {
        sol.selected_indices.push_back(indices[i]);
        sol.scores.push_back(scores[i]);
      }
      result.solutions.push_back(std::move(sol));
    }
  }

  // Build consensus ranking
  auto consensus = build_consensus(result.solutions, data.num_attributes());
  result.consensus_ranking = std::move(consensus.first);
  result.feature_frequencies = std::move(consensus.second);

  return result;
}

